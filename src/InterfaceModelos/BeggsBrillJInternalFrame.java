    public int[] rango() //Limites del eje y
    {
        int[] rango = new int[2];
        rango[0] = (int) Math.round(b.get(0));
        rango[1] = rango[0];
        for (int i = 1; i < b.size(); i++) {
            if (rango[0] > (int) Math.round(b.get(i))) {
                rango[0] = (int) Math.round(b.get(i));
            }
            if (rango[1] < (int) Math.round(b.get(i))) {
                rango[1] = (int) Math.round(b.get(i));
            }
        }
        return rango;
    }

    public void crearVectores() {
        objetoBeggsBrill = new Object[3];        
        double L = Longitud;
        for (int i = 0; i < 3; i++) {            
            beggsbrill = new BeggsBrill(L, P1, Tprom, R, Yg, Yw, YAPI, d, qL, Dp, Wcut,Bw,ug,uo,uw);           
            a.add(L);
            b.add(500 - beggsbrill.getDp());
            objetoBeggsBrill[i] = (Object) beggsbrill;
            L = L + 1000;    
        }
    }

